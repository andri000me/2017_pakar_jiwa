<h3 align="justify">PROFIL  POLI PSIKOLOG DI PUSKESMAS TANAH KALI KEDINDING SURABAYA</h3>


<p align="justify">	Puskesmas Tanah Kalikedinding adalah Puskesmas yang terletak di wilayah Kecamatan Kenjeran, dengan wilayah kerja satu kelurahan yakni kelurahan Tanah Kalikedinding yang terdiri dari 12 Rukun Warga dan 142 Rukun Tetangga.  Semua wilayah kerja puskesmas merupakan daerah dataran rendah dengan jumlah penduduk sekitar 58.250 jiwa. Puskesmas Tanah Kalikedinding juga memiliki unit Psikologi seabagai Unit pelayanan konseling kejiwaan, Mulai Maret 2014 hingga sekarang, Lita Arfandiyah Kusuma Dewi S.Psi bertugas sebagai Psikolog di Puskesmas Tanah Kalikedinding. Adapun jenis pelayanan psikologi yang dilakukan terdiri dari pelayanan konseling yang umumnya pasien dari usia anak-anak hingga lansia, Secara umum kasus yang dialami pasien ataupun kasus rujukan dari unit lain, misal dokter, serta pasien atas permintaan sendiri (APS) di Puskesmas Tanah Kalikedinding, selama ini kasus rujukan atau permintaan sendiri banyak yang mengeluh sakit secara fisik yang diagnosa Psikosa, Neurosa, Tumbuh Kembang, Masalah Belajar atau Sekolah dan Kenakalan Remaja dari pemeriksaan pasien menunjukan baik-baik saja, setelah diasesmen lebih lanjut, akhirnya dapat diketahui bahwa keluhan berasal dari permasalahan psikologis.</p>
<br>
<p align="justify">Semakin maju dan berkembangnya teknologi internet saat ini, semakin memudahkan pula para psikolog maupun pelajar psikologi dalam mengembangkan atau membagi ilmu-ilmu yang telah dimilikinya. Melalui fitur-fitur seperti website salah satunya yang menyediakan layanan psikologi online atau berkaitan di bidang psikologi.</p>
<br>
<p align="justify">Dengan adanya profil Poli Psikolog di Puskesmas Tanah Kalikedinding ini diharapkan dapat memberikan gambaran tentang pelayanan Poli Psikologi yang terdapat di Puskesmas Tanah Diharapkan informasi yang terangkum dalam profil Puskesmas ini dapat memenuhi kebutuhan informasi bagi perencana program, pelaksana program dan pengambil keputusan dalam membuat rencana kerja, prioritas program kerja dan memecahkan masalah yang dihadapi. Dari Profil Poli Psikolog puskesmas ini juga dapat mengetahui pelayanan kesehatan yang dibutuhkan oleh masyarakat sekitar dan menjadi pedoman bagi puskesmas dalam memberikan pelayanan yang lebih baik.
</p>
